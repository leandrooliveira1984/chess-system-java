package application;

import chess.ChessPiece;

public class UI {

	public static void printBoard(ChessPiece[][] pieces) {
		for (int i=0; i<pieces.length; i++) {
			System.out.print((8-i + " "));
			for (int j=0; j<pieces.length;j++) {
				printPiece(pieces[i][j]);
			}
			System.out.println();
		}
		System.out.println("  a b c d e f g h");
	}
	
	private static void printPiece(ChessPiece piece) {
		if (piece == null) { 
			System.out.print("-");
		}
		else {
			System.out.print(piece);
		}
		System.out.print(" ");
	} /*Método auxiliar para imprimir uma peça. 
	* se a peça for igual a nulo quer dizer que não tinha peça no tabuleiro >> imprime o "-";
	* caso contrario imprime a peça. No final imprimir um espaço em branco para 
	* que as peças não fiquem grudadas umas nas outras */
	
}
